void solve() {
    int n, q; cin >> n >> q;
    vector<ll> a(n + 1), pre(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    sort(all(a));
    for (int i = 1; i <= n; ++i) {
        pre[i] = pre[i - 1] + a[i];
    }
    while (q--) {
        ll x; cin >> x;
        int l = 1, r = n;
        int Ans = 0;
        while (l <= r) {
            int mid = (l + r) >> 1;
            // cout << mid << '\n';
            if (pre[mid] <= x) {
                Ans = mid;
                l = mid + 1;
            } else r = mid - 1;
        }
        cout << Ans << '\n';
    }
}

