void solve() {
    memset(dp, 0x3f3f3f3f, sizeof dp);
    ll x, k; cin >> x >> k;
    ll tmp = x;
    vector<ll> dig;
    // dfs(0, )
    while (tmp) {
        dig.pb(tmp % 10);
        tmp /= 10;
    }
    vector<int> pre(SZ(dig));
    for (int i = (SZ(pre) - 20); i >= 0; --i) {
        if (dig[i] == 9) pre[i] = pre[i + 1] + 1;
        else pre[i] = 0;
    }

}

